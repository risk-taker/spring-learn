package com.example.springdemo.com.springcore.ConstructorArg;

public class Certi {
    public String name;

    public Certi(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Certi{" +
                "name='" + name + '\'' +
                '}';
    }
}
